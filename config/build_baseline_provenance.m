%   params_ref   - baseline parameter struct from default_parameters      [-]
%
% OUTPUTS:
%   provenance   - table describing baseline source lineage              [-]
%
% NOTES:
%   - This is a scientific-audit helper, not a calibration routine.
%   - The current repo uses a hybrid foundation: Bozkurt-like chamber and
%     vascular magnitudes inside a Valenti-style 14-state circulation.
%
