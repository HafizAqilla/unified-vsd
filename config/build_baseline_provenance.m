    'Parameter', 'Group', 'Unit', 'CurrentValue', 'FoundationSource', ...
    'EvidenceScope', 'ConfidenceLevel', 'Notes'});
end

function row = make_row(params_ref, name, group_name, unit_name, source_name, scope_name, confidence_name, note_text)
row = { ...
    name, ...
    group_name, ...
    unit_name, ...
    get_nested_value(params_ref, name), ...
    source_name, ...
    scope_name, ...
    confidence_name, ...
    note_text};
end

function value = get_nested_value(params_ref, dotted_name)
parts = strsplit(dotted_name, '.');
value = params_ref;
for idx = 1:numel(parts)
    part = parts{idx};
    if isstruct(value) && isfield(value, part)
        value = value.(part);
    else
        error('build_baseline_provenance:missingField', ...
            'Unable to resolve parameter path %s.', dotted_name);
    end
end
end
